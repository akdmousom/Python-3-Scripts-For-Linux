#!/usr/bin/python3

"""

What is the (Subprocess) Module & what is it used for?

সহজ কথায় বলতে গেলে, subprocess হলো পাইথনের একটি জাদুকরী মডিউল যা পাইথনকে আপনার কম্পিউটারের অপারেটিং সিস্টেম বা টার্মিনালের (CMD/Terminal) সাথে কথা বলতে সাহায্য করে।
আপনি টার্মিনালে হাতে যে কমান্ডগুলো লেখেন (যেমন: ls, whoami, ping), সেই একই কমান্ডগুলো পাইথন স্ক্রিপ্টের ভেতর থেকে চালানোর জন্য এই মডিউলটি ব্যবহার করা হয়।

কেন এটি ব্যবহার করা হয়?

১. এক্সটার্নাল কমান্ড চালানো: পাইথন থেকেই লিনাক্স বা উইন্ডোজের কমান্ড চালানো।

২. আউটপুট ক্যাপচার করা: একটি কমান্ড রান করে তার রেজাল্ট বা আউটপুট পাইথন ভ্যারিয়েবলে জমা রাখা।

৩. অটোমেশন: বড় কোনো সফটওয়্যার বা সার্ভার কনফিগারেশন অটোমেট করা যেখানে সিস্টেম কমান্ড দরকার হয়।

৪. ইনপুট পাঠানো: পাইথন থেকে কোনো কমান্ডে ডেটা পাঠানো (যেমন কোনো পাসওয়ার্ড বা টেক্সট ইনপুট দেওয়া)।

os.system() বনাম subprocess
আপনি আগে os.system() ব্যবহার করেছিলেন। কিন্তু প্রফেশনালরা subprocess ব্যবহার করে কারণ:

এটি বেশি নিরাপদ (Security)।

এটি আপনাকে আউটপুট এবং এরর আলাদাভাবে হ্যান্ডেল করার ক্ষমতা দেয়।

এটি আপনার স্ক্রিপ্টকে বেশি শক্তিশালী করে তোলে।

এক নজরে মূল পয়েন্টগুলো:

subprocess.run(): কমান্ড চালানোর প্রধান ফাংশন।

args: কমান্ডের লিস্ট (যেমন: ["ping", "google.com"])।

capture_output=True: কমান্ডের রেজাল্ট পাইথনে ফেরত আনতে এটি লাগে।

text=True: রেজাল্টকে বাইট (Bytes) থেকে টেক্সটে রূপান্তর করতে ব্যবহৃত হয়।

সারসংক্ষেপ: আপনি যখনই পাইথনকে দিয়ে টার্মিনালের কোনো কাজ করাতে চাইবেন, তখনই আপনার প্রিয় বন্ধু হবে এই subprocess মডিউল।

"""


import subprocess

user = subprocess.run(["whoami"], capture_output=True, text=True)

print(f"="*30)
print(f"Your User Name IS: {user.stdout.strip()}")
print("=" * 30)